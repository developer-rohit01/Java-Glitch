//wrapper class for Assignment 03, question 1

public class Assignment_03_1 {
  public static void main(String[] args) {
    // premitive data types
    int a = 10;
    double b = 20.5;

    // wrapper classes
    Integer aWrapper = Integer.valueOf(a);
    Double bWrapper = Double.valueOf(b);

    System.out.println("Primitive int: " + a);
    System.out.println("Wrapper Integer: " + aWrapper);
    System.out.println("Primitive double: " + b);
    System.out.println("Wrapper Double: " + bWrapper);

    

  }
  
}
